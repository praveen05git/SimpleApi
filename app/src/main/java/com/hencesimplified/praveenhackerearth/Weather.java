package com.hencesimplified.praveenhackerearth;

public class Weather {

    public String temp;
    public String time;
    public String rain;
    public String wind;

    public Weather(String temp, String time, String rain, String wind) {
        this.temp = temp;
        this.time = time;
        this.rain = rain;
        this.wind = wind;
    }
}
